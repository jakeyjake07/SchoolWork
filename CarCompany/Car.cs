namespace CarCompany
{
    public class Car
    {
        public string Brand;
        public string Model;
        public string Color;
        public string FuelType;
        public string Body;


        public Car(string brand, string model, string color, string fuelType, string body)
        {
            Brand = brand;
            Model = model;
            Color = color;
            FuelType = fuelType;
            Body = body;
        }

    }

}